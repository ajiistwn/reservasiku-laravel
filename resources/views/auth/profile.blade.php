    $('#btn-pay').on('click', function () {
        var snapToken = $(this).data('token');

        snap.pay(snapToken, {
            onSuccess: function(result){
                $.post('/midtrans/update', {
                    _token: '{{ csrf_token() }}',
                    order_id: orderId,
                    transaction_status: 'success',
                }, function(response) {
                    console.log(JSON.stringify(response)); // Fixed the console.log statement
                    Swal.fire({
                        icon: 'success',
                        title: response.data,
                        text: 'Thank you! Your payment was successful.',
                    }).then(() => {
                        location.reload();
                    });
                });
            },
            onPending: function(result){
                $.post('/midtrans/update', {
                    _token: '{{ csrf_token() }}',
                    order_id: orderId,
                    transaction_status: 'pending',
                }, function(response) {
                    Swal.fire({
                        icon: 'info',
                        title: 'Waiting for payment',
                        text: 'Please complete the payment.',
                    }).then(() => {
                        location.reload();
                    });
                });
            },
            onError: function(result){
                $.post('/midtrans/update', {
                    _token: '{{ csrf_token() }}',
                    order_id: orderId,
                    transaction_status: 'error',
                }, function(response) {
                    Swal.fire({
                        icon: 'error',
                        title: 'Payment Error',
                        text: 'Sorry for the inconvenience, please restart payment.',
                    }).then(() => {
                        location.reload();
                    });
                });
            },
            onClose: function(){
                Swal.fire({
                    icon: 'info',
                    title: 'Payment Closed',
                    text: 'You closed the payment popup. Please complete the payment later.',
                }).then(() => {
                    location.reload();
                });
            }
        });
    });


</script>
